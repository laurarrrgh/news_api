describe("makeRefObj", () => {
  it("return a lookup object for one record", () => {
    const listArray = [{ article_id: 1, title: "A" }];
    const refObj = { A: 1 };
    expect(makeRefObj(listArray)).to.eql(refObj);
  });
  it("return a lookup object for multiple records", () => {
    const listArray = [
      { article_id: 1, title: "A" },
      { article_id: 2, title: "B" },
      { article_id: 3, title: "C" }
    ];
    const refObj = { A: 1, B: 2, C: 3 };
    expect(makeRefObj(listArray)).to.eql(refObj);
  });
});

describe("formatComments", () => {
  it("returns a new comment obj", () => {
    const reference = { "They're not exactly dogs, are they?": 9 };
    const originalComment = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        belongs_to: "They're not exactly dogs, are they?",
        created_by: "butter_bridge",
        votes: 16,
        created_at: 1511354163389
      }
    ];
    const amendedComment = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        article_id: 9,
        author: "butter_bridge",
        votes: 16,
        created_at: new Date(1511354163389)
      }
    ];

    expect(formatComments(originalComment, reference)).to.eql(amendedComment);
  });
});
